#include "SAMP-EDGEnginePCH.hpp"

// The header of this class:
#include <SAMP-EDGEngine/World/WI3DNode.hpp>

namespace samp_edgengine
{

////////////////////////////////////////////////////////////////////////
IWI3DNode::IWI3DNode()
	:
	m_world{ 0 },
	m_interior{ 0 }
{
}

////////////////////////////////////////////////////////////////////////
void IWI3DNode::setWorld(Int32 world_)
{
	this->syncWorld(world_);
}

////////////////////////////////////////////////////////////////////////
void IWI3DNode::setInterior(Int32 interior_)
{
	this->syncInterior(interior_);
}

////////////////////////////////////////////////////////////////////////
Int32 IWI3DNode::getWorld() const
{
	return m_world;
}

////////////////////////////////////////////////////////////////////////
Int32 IWI3DNode::getInterior() const
{
	return m_interior;
}

////////////////////////////////////////////////////////////////////////
void IWI3DNode::syncWorld(Int32 world_)
{
	m_world = world_;
}

////////////////////////////////////////////////////////////////////////
void IWI3DNode::syncInterior(Int32 interior_)
{
	m_interior = interior_;
}

} // namespace agdk